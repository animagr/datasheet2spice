export const DIALECTS = ["common", "ltspice", "ngspice", "pspice", "hspice", "xyce", "qspice"];

export const MODEL_EMITTER_MODULE = {
  id: "browser-spice-starter-emitter",
  kind: "model-emitter",
  label: "Browser SPICE Starter Emitter",
  version: "0.1.0",
  description: "Static browser-side ABM and VDMOS starter bundle generation.",
  component_profiles: ["mosfet.power", "diode.power"],
  runtime_modes: ["browser-pages"],
  capabilities: ["emit_model_bundle", "emit_abm_basic", "emit_vdmos_static_fast", "emit_diode_basic", "emit_diode_abm_dynamic"],
  dialects: DIALECTS,
  generateBundle,
  modelName
};

export function generateBundle(project, models, dialects) {
  const files = {};
  for (const model of models) {
    for (const dialect of dialects) {
      if (model === "abm-basic") Object.assign(files, emitAbm(project, dialect));
      if (model === "vdmos-static-fast") Object.assign(files, emitVdmos(project, dialect));
      if (model === "diode-basic") Object.assign(files, emitDiode(project, dialect));
      if (model === "diode-abm-dynamic") Object.assign(files, emitDiodeAbm(project, dialect));
    }
  }
  files[`${modelName(project)}_project.json`] = JSON.stringify(project, null, 2) + "\n";
  files[`${modelName(project)}_README.txt`] = renderReport(project, models, dialects);
  return files;
}

export function modelName(project) {
  return cleanName(project.device?.part_number || "DEVICE");
}

function emitAbm(project, dialect) {
  const name = modelName(project);
  const params = modelParams(project);
  const file = `${name}_abm${suffix(dialect)}.lib`;
  const deck = `${name}_double_pulse_abm${suffix(dialect)}.cir`;
  const source = dialect === "pspice" || dialect === "common"
    ? "Gch d_int s_int VALUE = {IDSAT_F(V(g_int,s_int))*tanh(V(d_int,s_int)/(RON*IDSAT_F(V(g_int,s_int))+1m))}"
    : dialect === "hspice"
      ? "Gch d_int s_int CUR='IDSAT_F(V(g_int,s_int))*tanh(V(d_int,s_int)/(RON*IDSAT_F(V(g_int,s_int))+1m))'"
      : "Bch d_int s_int I = {IDSAT_F(V(g_int,s_int))*tanh(V(d_int,s_int)/(RON*IDSAT_F(V(g_int,s_int))+1m))}";
  const cgsLine = dialect === "pspice" || dialect === "common"
    ? "Ggs g_int s_int VALUE = {CGS_F(V(d_int,s_int))*DDT(V(g_int,s_int))}"
    : dialect === "hspice"
      ? "Ggs g_int s_int CUR='CGS_F(V(d_int,s_int))*DDT(V(g_int,s_int))'"
      : `Bgs g_int s_int I = {CGS_F(V(d_int,s_int))*${dialect === "ltspice" || dialect === "qspice" ? "ddt" : "DDT"}(V(g_int,s_int))}`;
  const cgdLine = dialect === "pspice" || dialect === "common"
    ? "Ggd g_int d_int VALUE = {CGD_F(V(d_int,s_int))*DDT(V(g_int,d_int))}"
    : dialect === "hspice"
      ? "Ggd g_int d_int CUR='CGD_F(V(d_int,s_int))*DDT(V(g_int,d_int))'"
      : `Bgd g_int d_int I = {CGD_F(V(d_int,s_int))*${dialect === "ltspice" || dialect === "qspice" ? "ddt" : "DDT"}(V(g_int,d_int))}`;
  const cdsLine = dialect === "pspice" || dialect === "common"
    ? "Gds d_int s_int VALUE = {CDS_F(V(d_int,s_int))*DDT(V(d_int,s_int))}"
    : dialect === "hspice"
      ? "Gds d_int s_int CUR='CDS_F(V(d_int,s_int))*DDT(V(d_int,s_int))'"
      : `Bds d_int s_int I = {CDS_F(V(d_int,s_int))*${dialect === "ltspice" || dialect === "qspice" ? "ddt" : "DDT"}(V(d_int,s_int))}`;
  const clampExpr = dialect === "ltspice" || dialect === "qspice"
    ? "limit(abs(v),0.1,1000)"
    : "min(max(abs(v),0.1),1000)";
  const lib = clean(`
    * ${name} ABM dynamic-basic model generated by datasheet2spice Web Workbench
    * Dialect: ${dialect}. Browser output mirrors the Python ABM starter semantics.
    * Review against datasheet and lab waveforms before design use.
    .subckt ${name} D G S PARAMS: TJ=25 LD=${params.ld}n LS=${params.ls}n LG=${params.lg}n RGEXT=${params.rgExt} RON_SCALE=1 CGS_SCALE=1 CGD_SCALE=1 CDS_SCALE=1 VSMOOTH=0.35
    .param RGINT=${params.rg}
    .param KID=${params.kid}
    .param RON={RON_SCALE*table(TJ, ${params.rdsTable})}
    .param VTH={table(TJ, ${params.vthTable})}
    .func VDSX(v) {${clampExpr}}
    .func VOV_F(vgs) {0.5*((vgs-VTH)+sqrt((vgs-VTH)*(vgs-VTH)+VSMOOTH*VSMOOTH))}
    .func IDSAT_F(vgs) {max(KID*VOV_F(vgs)*VOV_F(vgs),1u)}
    .func CISS_F(v) {table(VDSX(v), ${params.cissTable})}
    .func COSS_F(v) {table(VDSX(v), ${params.cossTable})}
    .func CRSS_F(v) {table(VDSX(v), ${params.crssTable})}
    .func CGS_F(v) {CGS_SCALE*max(CISS_F(v)-CRSS_F(v),1p)}
    .func CGD_F(v) {CGD_SCALE*max(CRSS_F(v),1p)}
    .func CDS_F(v) {CDS_SCALE*max(COSS_F(v)-CRSS_F(v),1p)}
    Ld D d_int {LD}
    Lg G g_pad {LG}
    Ls S s_int {LS}
    Rg g_pad g_int {RGINT+RGEXT}
    ${source}
    ${cgsLine}
    ${cgdLine}
    ${cdsLine}
    Dbody s_int d_int DBODY
    .model DBODY D(Is=1e-12 N=2 Rs=31m Cjo=${params.cjo}p M=0.45 Vj=1.0 Tt=${params.tt}n BV=${params.vdss} Ibv=30m)
    Rgate_leak g_int s_int 1e12
    .ends ${name}
  `);
  return { [file]: lib, [deck]: starterDeck(project, file, `XQ drain gate 0 ${name} PARAMS: TJ=25 LG=${params.lg}n RGEXT=0`, "ABM") };
}

function emitVdmos(project, dialect) {
  const name = modelName(project);
  const params = modelParams(project);
  const model = `${name}_VDMOS`;
  const file = `${name}_vdmos${suffix(dialect)}.lib`;
  const deck = `${name}_double_pulse_vdmos${suffix(dialect)}.cir`;
  const native = dialect === "ltspice" || dialect === "ngspice";
  const lib = native ? clean(`
    * ${model} native VDMOS starter generated by datasheet2spice Web Workbench
    .model ${model} VDMOS(Rd=${params.rd} Rs=${params.rs} Rg=${params.rg} Vto=${params.vth} Kp=${params.kp}
    + Cgdmax=${params.cgdmax}p Cgdmin=${params.cgdmin}p Cgs=${params.cgs}p Cjo=${params.cjo}p
    + Is=1e-12 N=2 Rb=1m Tt=${params.tt}n BV=${params.vdss} Ibv=30m)
  `) : clean(`
    * ${model} portable MOS fallback generated by datasheet2spice Web Workbench
    * Dialect: ${dialect}. This is not a native VDMOS model.
    .subckt ${model} D G S
    Rd D d_mos ${params.rd}
    Rs s_mos S ${params.rs}
    Rg G g_int ${Math.max(params.rg, 1e-6)}
    Mch d_mos g_int s_mos s_mos ${model}_NMOS L=1u W=1u
    Cgs g_int S ${params.cgs}p
    Cgd g_int D ${params.cgd}p
    Cds D S ${params.cjo}p
    Dbody S D ${model}_DBODY
    .model ${model}_NMOS NMOS(Level=1 Vto=${params.vth} Kp=${params.kp} Lambda=0.002)
    .model ${model}_DBODY D(Is=1e-12 N=2 Rs=31m Cjo=${params.cjo}p Tt=${params.tt}n BV=${params.vdss} Ibv=30m)
    .ends ${model}
  `);
  const instance = native ? `M1 drain gate 0 ${model}` : `XQ drain gate 0 ${model}`;
  return { [file]: lib, [deck]: starterDeck(project, file, instance, "VDMOS") };
}

function emitDiode(project, dialect) {
  const name = modelName(project);
  const params = diodeParams(project);
  const file = `${name}_diode${suffix(dialect)}.lib`;
  const deck = `${name}_reverse_recovery_diode${suffix(dialect)}.cir`;
  const lib = clean(`
    * ${name} diode-basic model generated by datasheet2spice Web Workbench
    * Dialect: ${dialect}. Uses portable two-terminal SPICE diode primitives.
    * Pins: A K
    .subckt ${name} A K
    La A a_lead ${params.la}n
    RpkgA a_lead a_int ${params.ra}
    Dcore a_int k_int ${name}_DIODE
    RpkgK k_int k_lead ${params.rk}
    Lk k_lead K ${params.lk}n
    .model ${name}_DIODE D(Is=${params.is} N=${params.n} Rs=${params.rs} Cjo=${params.cjo}p
    + M=0.45 Vj=0.8 Tt=${params.tt}n BV=${params.bv} Ibv=${params.ibv})
    .ends ${name}
  `);
  const forwardV = Math.max(params.vf * 1.1, 0.8);
  const reverseV = Math.min(params.bv * 0.25, 400);
  const sourceR = Math.max(forwardV / Math.max(params.ifRef, 1e-9), 0.001);
  const testDeck = clean(`
    * ${name} diode reverse-recovery starter deck
    .include ${file}
    Vstep drive 0 PULSE(${forwardV} ${-reverseV} 100n 10n 10n 1u 3u 1)
    Rsrc drive anode ${sourceR}
    X1 anode 0 ${name}
    .tran 0 4.5u 0 1n
    .save V(anode) I(Vstep)
    .options plotwinsize=0 reltol=0.003 abstol=1n vntol=1u
    .end
  `);
  return { [file]: lib, [deck]: testDeck };
}

function emitDiodeAbm(project, dialect) {
  const name = modelName(project);
  const params = diodeParams(project);
  const file = `${name}_diode_abm${suffix(dialect)}.lib`;
  const deck = `${name}_reverse_recovery_diode_abm${suffix(dialect)}.cir`;
  const fwdCurrent = "0.5*(I(Vsense)+sqrt(I(Vsense)*I(Vsense)+1e-18))";
  const reverseVoltage = "0.5*(V(k_int,a_sense)+sqrt(V(k_int,a_sense)*V(k_int,a_sense)+1e-12))";
  const reverseGate = "0.5*(1-tanh(V(a_sense,k_int)/VSMOOTH))";
  const cjExpr = `CJ_SCALE*CJO*(1+${reverseVoltage}/VJ)**(-M)*DDT(V(a_sense,k_int))`;
  const qExpr = `V(qrr_state)/TAU - (1-(${reverseGate}))*${fwdCurrent}`;
  const recoveryExpr = `-RR_SCALE*(${reverseGate})*V(qrr_state)/TAU`;
  const lib = clean(`
    * ${name} diode-abm-dynamic model generated by datasheet2spice Web Workbench
    * Dialect: ${dialect}. Behavioral starter for Cj(VR) and reverse-recovery transient checks.
    * Pins: A K
    .subckt ${name} A K PARAMS: RR_SCALE=${params.rrScale} CJ_SCALE=1 VSMOOTH=50m
    .param TAU=${params.tau}n CJO=${params.cjo}p VJ=0.8 M=0.45
    La A a_lead ${params.la}n
    RpkgA a_lead a_int ${params.ra}
    Vsense a_int a_sense 0
    Ddc a_sense k_int ${name}_DC
    ${behaviorCurrent("cj", "a_sense", "k_int", cjExpr, dialect)}
    ${behaviorCurrent("qrr", "qrr_state", "0", qExpr, dialect)}
    Cqrr qrr_state 0 1
    Rqrr qrr_state 0 1e12
    ${behaviorCurrent("rr", "a_sense", "k_int", recoveryExpr, dialect)}
    Rleak a_sense k_int 1e12
    RpkgK k_int k_lead ${params.rk}
    Lk k_lead K ${params.lk}n
    .model ${name}_DC D(Is=${params.is} N=${params.n} Rs=${params.rs} Cjo=1f
    + M=0.45 Vj=0.8 Tt=0 BV=${params.bv} Ibv=${params.ibv})
    .ends ${name}
  `);
  return { [file]: lib, [deck]: diodeRecoveryDeck(project, file, params) };
}

function diodeRecoveryDeck(project, file, params) {
  const name = modelName(project);
  const forwardV = Math.max(params.vf * 1.1, 0.8);
  const reverseV = Math.min(params.bv * 0.25, 400);
  const sourceR = Math.max(forwardV / Math.max(params.ifRef, 1e-9), 0.001);
  return clean(`
    * ${name} diode reverse-recovery starter deck
    .include ${file}
    Vstep drive 0 PULSE(${forwardV} ${-reverseV} 100n 10n 10n 1u 3u 1)
    Rsrc drive anode ${sourceR}
    X1 anode 0 ${name}
    .tran 0 4.5u 0 1n
    .save V(anode) I(Vstep)
    .options plotwinsize=0 reltol=0.003 abstol=1n vntol=1u
    .end
  `);
}

function behaviorCurrent(name, pos, neg, expr, dialect) {
  if (dialect === "pspice" || dialect === "common") return `G${name} ${pos} ${neg} VALUE = {${expr}}`;
  if (dialect === "hspice") return `G${name} ${pos} ${neg} CUR='${expr}'`;
  return `B${name} ${pos} ${neg} I = {${expr}}`;
}

function starterDeck(project, libFile, deviceLine, family) {
  const name = modelName(project);
  const vdss = num(getPath(project, "ratings.vdss_v"), 1200);
  const vbus = Math.min(vdss * 2 / 3, 800);
  const vOn = num(getPath(project, "ratings.vgs_on_v"), 10);
  const vOff = num(getPath(project, "ratings.vgs_off_v"), 0);
  const rgDrive = num(getPath(project, "parasitics.rg_ext_ohm"), 4.7);
  return clean(`
    * ${name} ${family} low-side inductive double-pulse starter deck
    .include ${libFile}
    Vbus bus 0 ${vbus}
    Rload bus nload 20m
    Lload nload drain 250u
    Dfw drain bus DFAST
    .model DFAST D(Is=1e-15 N=1.5 Rs=20m Cjo=80p Tt=20n BV=${vdss} Ibv=1m)
    Vdrv gate_drv 0 PULSE(${vOff} ${vOn} 100n 5n 5n 21u 25u 2)
    Rdrv gate_drv gate ${rgDrive}
    ${deviceLine}
    .tran 0 26.5u 0 1n
    .save V(drain) V(gate) V(gate_drv) I(Lload) I(Vbus)
    .options plotwinsize=0 reltol=0.003 abstol=1n vntol=1u
    .end
  `);
}

function renderReport(project, models, dialects) {
  return [
    "datasheet2spice Web Workbench report",
    `Part: ${modelName(project)}`,
    `Models: ${models.join(", ")}`,
    `Dialects: ${dialects.join(", ")}`,
    "",
    "Generated models are starter netlists, not vendor-qualified models.",
    "Review all extracted values and validate against datasheet or lab waveforms before design use."
  ].join("\n") + "\n";
}

function modelParams(project) {
  const rds = num(getPath(project, "static.rds_on_mohm.25"), 20) * 1e-3;
  const gfs = num(getPath(project, "static.gfs_s"), 20);
  const staticData = getPath(project, "static", {});
  const caps = getPath(project, "dynamic.capacitance", {});
  const ciss = (caps.ciss_pf || [2000]).map(Number);
  const coss = (caps.coss_pf || [300]).map(Number);
  const crss = (caps.crss_pf || [30]).map(Number);
  const vds = (caps.vds_v || [0.1, 1000]).map(Number);
  const channel = getPath(project, "dynamic.channel_fit", {});
  const body = getPath(project, "dynamic.body_diode", {});
  const last = (values, fallback) => values.length ? num(values[values.length - 1], fallback) : fallback;
  const vth = num(getPath(project, "static.vgs_th_v.25"), 4);
  const idsRef = num(channel.idsat_reference_a, 50);
  const vgsRef = num(channel.vgs_reference_v, 10);
  const qrr = num(body.qrr_nc, 0);
  const trr = num(body.trr_ns, 30);
  return {
    vth,
    rds,
    rd: Math.max(rds * 0.45, 1e-6),
    rs: Math.max(rds * 0.45, 1e-6),
    rg: num(getPath(project, "static.rg_int_ohm"), 1),
    rgExt: num(getPath(project, "parasitics.rg_ext_ohm"), 4.7),
    ld: num(getPath(project, "parasitics.ld_nh"), 2),
    ls: num(getPath(project, "parasitics.ls_nh"), 1),
    lg: num(getPath(project, "parasitics.lg_nh"), 0.2),
    kp: Math.max(gfs * gfs * rds / 2, 0.1),
    kid: idsRef / Math.max((vgsRef - vth) ** 2, 1e-6),
    rdsTable: tempTable(staticData.rds_on_mohm || { "25": 20 }, "m"),
    vthTable: tempTable(staticData.vgs_th_v || { "25": 4 }),
    cissTable: tablePairs(vds, ciss, "p"),
    cossTable: tablePairs(vds, coss, "p"),
    crssTable: tablePairs(vds, crss, "p"),
    cgdmax: Math.max(...crss),
    cgdmin: Math.min(...crss.slice(-3)),
    cgd: Math.max(last(crss, 30), 1),
    cgs: Math.max(last(ciss, 2000) - last(crss, 30), 1),
    cjo: Math.max(last(coss, 300) - last(crss, 30), 1),
    vdss: num(getPath(project, "ratings.vdss_v"), 1200),
    tt: qrr > 0 && idsRef > 0 ? qrr / idsRef : trr
  };
}

function tempTable(values, valueSuffix = "") {
  return Object.entries(values)
    .map(([temperature, value]) => [Number(temperature), Number(value)])
    .filter(([temperature, value]) => Number.isFinite(temperature) && Number.isFinite(value))
    .sort((a, b) => a[0] - b[0])
    .map(([temperature, value]) => `${formatNumber(temperature)},${formatNumber(value)}${valueSuffix}`)
    .join(", ");
}

function tablePairs(xs, ys, valueSuffix = "") {
  const count = Math.max(xs.length, ys.length, 1);
  const pairs = [];
  for (let index = 0; index < count; index += 1) {
    const x = num(xs[Math.min(index, xs.length - 1)], index ? 1000 : 0.1);
    const y = num(ys[Math.min(index, ys.length - 1)], 1);
    pairs.push(`${formatNumber(x)},${formatNumber(y)}${valueSuffix}`);
  }
  return pairs.join(", ");
}

function diodeParams(project) {
  const bv = num(getPath(project, "ratings.vrrm_v", getPath(project, "ratings.vr_v")), 600);
  const ifRef = num(getPath(project, "static.forward_voltage.if_a", getPath(project, "ratings.if_av_a")), 10);
  const vf = num(getPath(project, "static.forward_voltage.vf_v"), 1);
  const cjo = num(getPath(project, "dynamic.junction_capacitance.cj0_pf", getPath(project, "dynamic.junction_capacitance.cj_pf")), 50);
  const trr = num(getPath(project, "dynamic.reverse_recovery.trr_ns"), 20);
  const qrr = num(getPath(project, "dynamic.reverse_recovery.qrr_nc"), 0);
  let irrm = num(getPath(project, "dynamic.reverse_recovery.irrm_a"), NaN);
  const n = 1.8;
  const rs = Math.max((vf / Math.max(ifRef, 1e-9)) * 0.08, 1e-6);
  const exponent = Math.min(Math.max((vf - ifRef * rs) / (n * 0.025852), 1), 80);
  const is = Math.min(Math.max(ifRef / Math.max(Math.exp(exponent) - 1, 1e-30), 1e-18), 1e-3);
  const tt = qrr > 0 ? Math.max(qrr / Math.max(ifRef, 1e-9), trr * 0.35) : trr;
  if (!Number.isFinite(irrm) || irrm <= 0) irrm = qrr > 0 ? 2 * qrr / Math.max(trr, 1e-9) : ifRef;
  return {
    bv: Math.max(bv, 1),
    ifRef: Math.max(ifRef, 1e-6),
    vf: Math.max(vf, 0.05),
    cjo: Math.max(cjo, 0.01),
    trr,
    qrr,
    n,
    rs,
    is,
    tt: Math.max(tt, 0.001),
    tau: Math.max(tt, 0.001),
    irrm: Math.max(irrm, 0),
    rrScale: clamp(irrm / Math.max(ifRef, 1e-9), 0.05, 10),
    ibv: Math.max(num(getPath(project, "static.leakage.ir_ua"), 10) * 1e-6, 1e-9),
    la: Math.max(num(getPath(project, "parasitics.la_nh"), 1), 1e-6),
    lk: Math.max(num(getPath(project, "parasitics.lk_nh"), 1), 1e-6),
    ra: Math.max(num(getPath(project, "parasitics.ra_ohm"), 0.001), 1e-6),
    rk: Math.max(num(getPath(project, "parasitics.rk_ohm"), 0.001), 1e-6)
  };
}

function cleanName(text) {
  return String(text || "DEVICE").toUpperCase().replace(/[^A-Z0-9_]+/g, "_").replace(/^_+|_+$/g, "") || "DEVICE";
}

function suffix(dialect) {
  return dialect === "common" ? "" : `_${dialect}`;
}

function num(value, fallback) {
  const parsed = Number(value);
  return Number.isFinite(parsed) ? parsed : fallback;
}

function formatNumber(value) {
  const parsed = Number(value);
  if (!Number.isFinite(parsed)) return "0";
  return Number.parseFloat(parsed.toPrecision(8)).toString();
}

function getPath(obj, path, fallback = undefined) {
  return path.split(".").reduce((cur, key) => cur && cur[key] !== undefined ? cur[key] : undefined, obj) ?? fallback;
}

function clean(text) {
  return text.split("\n").map(line => line.trim()).filter(Boolean).join("\n") + "\n";
}

function clamp(value, low, high) {
  return Math.min(Math.max(value, low), high);
}
